// VAR
var firstName; // Undefined
firstName = 'Tomás';
console.log(firstName);

var lastName = 'Orrego'; // declarar + asignar
lastName = 'Alvarez'; // reasignar
console.log(firstName +' '+ lastName);

// LET
let fruit = 'Apple'; // declarar + asignar
fruit = 'Kiwi'; // Reasignación
console.log(fruit);
//let fruit = 'Banana'; // La redeclaración con let no está permitida

// CONST
const favBand = 'Iron Maiden'; // declarar + asignar
//const favBand = 'Judas Priest'; // La redeclaración con const no está permitida
//favBand = 'Judas Priest'; // La reasignación con const no está permitida
console.log(favBand);
